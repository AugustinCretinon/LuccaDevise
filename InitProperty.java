package com.lucca.devise;

public class InitProperty {
	private String initCurrency;
	private double initAmount;
	private String goalCurrency;
	
	public String getInitCurrency() {
		return initCurrency;
	}
	public void setInitCurrency(String initCurrency) {
		this.initCurrency = initCurrency;
	}
	public double getInitAmount() {
		return initAmount;
	}
	public void setInitAmount(double initAmount) {
		this.initAmount = initAmount;
	}
	public String getGoalCurrency() {
		return goalCurrency;
	}
	public void setGoalCurrency(String goalCurrency) {
		this.goalCurrency = goalCurrency;
	}
}
