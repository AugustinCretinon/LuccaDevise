package com.lucca.devise;

public class InitProperty {
	private String sInitCurrency;
	private String sInitAmount;
	private String sGoalCurrency;
	
	public String getsInitCurrency() {
		return sInitCurrency;
	}
	public void setsInitCurrency(String sInitCurrency) {
		this.sInitCurrency = sInitCurrency;
	}
	public String getsInitAmount() {
		return sInitAmount;
	}
	public void setsInitAmount(String sInitAmount) {
		this.sInitAmount = sInitAmount;
	}
	public String getsGoalCurrency() {
		return sGoalCurrency;
	}
	public void setsGoalCurrency(String sGoalCurrency) {
		this.sGoalCurrency = sGoalCurrency;
	}
	

	
}
